package monday;

public class L4_Arithmetic {
	public static void main(String[] args) {
	
		// + is for addition
		// - is for subtraction
		// * is for multiplication
		// / is for division
		// % is a modulus (finds the remainder)
		// ++ is for increment
		// -- is for decrement

		
		int num = 100;
		int factor = 20;
		int sum = 0;

		sum = num + factor;
		System.out.println("addtion sum: " + sum);
		sum = num - factor;
		System.out.println("Subtraction sum: " + sum);

		sum = num * factor;
		System.out.println("Multiplication sum: " + sum);

		sum = num/factor;
		System.out.println("Division sum: " + sum);
		
		
	}
}
